def check_plagiarism(resumes_data):
    """Compares resumes against each other to find matches."""
    if len(resumes_data) < 2:
        return ["No duplicates (Only one resume)"] * len(resumes_data)

    texts = [r['text'] for r in resumes_data]
    tfidf = TfidfVectorizer().fit_transform(texts)
    matrix = cosine_similarity(tfidf)

    flags = []
    for i in range(len(resumes_data)):
        # Find highest similarity with any OTHER resume
        others = [matrix[i][j] for j in range(len(resumes_data)) if i != j]
        max_sim = max(others) if others else 0
        if max_sim > 0.8:
            flags.append(f"⚠️ HIGH ({int(max_sim*100)}%)")
        elif max_sim > 0.5:
            flags.append(f"🟡 MODERATE ({int(max_sim*100)}%)")
        else:
            flags.append("✅ Low")
    return flags

def process_hiring(jd, files):
    if not jd or not files:
        return pd.DataFrame(), None

    resumes_data = []
    for f in files:
        resumes_data.append({
            "name": f.name.split('/')[-1],
            "path": f.name,
            "text": extract_text(f.name)
        })

    # Calculate JD Match
    all_texts = [jd] + [r['text'] for r in resumes_data]
    vectorizer = TfidfVectorizer(stop_words='english')
    tfidf_matrix = vectorizer.fit_transform(all_texts)
    scores = cosine_similarity(tfidf_matrix[0:1], tfidf_matrix[1:]).flatten()

    # Calculate Plagiarism
    plagiarism_report = check_plagiarism(resumes_data)

    # Create DataFrame
    df = pd.DataFrame({
        "Rank": range(1, len(files) + 1),
        "Candidate Name": [r['name'] for r in resumes_data],
        "Match Score (%)": [round(s * 100, 2) for s in scores],
        "Plagiarism Risk": plagiarism_report,
        "File_Path": [r['path'] for r in resumes_data] # Hidden helper column
    }).sort_values(by="Match Score (%)", ascending=False)

    # Reset rank after sorting
    df["Rank"] = range(1, len(df) + 1)
    return df

