def process_and_rank(job_description, resume_files):
    if not job_description or not resume_files:
        return "Please provide both a Job Description and at least one Resume."

    # 1. Prepare data
    resume_texts = []
    resume_names = []

    for file in resume_files:
        # Extract name from file path or object
        name = file.name.split('/')[-1]
        text = extract_text_from_pdf(file)
        resume_texts.append(text)
        resume_names.append(name)

    # 2. Add JD to the list for vectorization
    all_texts = [job_description] + resume_texts

    # 3. Calculate Similarity
    vectorizer = TfidfVectorizer(stop_words='english')
    tfidf_matrix = vectorizer.fit_transform(all_texts)

    # Compare first row (JD) with the rest (Resumes)
    scores = cosine_similarity(tfidf_matrix[0:1], tfidf_matrix[1:]).flatten()

    # 4. Create Leaderboard
    results = pd.DataFrame({
        "Candidate Name": resume_names,
        "Match Score (%)": [round(s * 100, 2) for s in scores]
    })

    # Sort by highest score
    return results.sort_values(by="Match Score (%)", ascending=False)

